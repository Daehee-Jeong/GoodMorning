public class TMObject {
    int tmX;
    int tmY;
    public TMObject(int tmX, int tmY) {
        this.tmX = tmX;
        this.tmY = tmY;
    }
    public int getTmX() {
        return tmX;
    }
    public void setTmX(int tmX) {
        this.tmX = tmX;
    }
    public int getTmY() {
        return tmY;
    }
    public void setTmY(int tmY) {
        this.tmY = tmY;
    }
} // end of inner class TMObject
